import { Request, Response } from 'express';
import { PrismaClient, Mood, Privacy } from '@prisma/client';
import { validationResult } from 'express-validator';

const prisma = new PrismaClient();

interface CreateEntryRequest {
  title?: string;
  content: string;
  mood?: Mood;
  privacy?: Privacy;
  location?: string;
  weather?: string;
  tags?: string[];
  isFavorite?: boolean;
}

interface UpdateEntryRequest extends Partial<CreateEntryRequest> {}

interface QueryParams {
  page?: string;
  limit?: string;
  search?: string;
  mood?: Mood;
  startDate?: string;
  endDate?: string;
  tags?: string;
  isFavorite?: string;
  sortBy?: 'createdAt' | 'updatedAt' | 'title';
  sortOrder?: 'asc' | 'desc';
}

// Create a new entry
export const createEntry = async (req: Request<{}, {}, CreateEntryRequest>, res: Response) => {
  try {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({ errors: errors.array() });
    }

    if (!req.user) {
      return res.status(401).json({ error: 'Not authenticated' });
    }

    const { title, content, mood, privacy, location, weather, tags, isFavorite } = req.body;

    // Calculate word count
    const wordCount = content.trim().split(/\s+/).length;

    // Start a transaction to create entry and tags
    const entry = await prisma.$transaction(async (tx) => {
      // Create the entry
      const newEntry = await tx.entry.create({
        data: {
          userId: req.user!.id,
          title,
          content,
          mood,
          privacy: privacy || Privacy.PRIVATE,
          location,
          weather,
          wordCount,
          isFavorite: isFavorite || false,
        },
        include: {
          tags: {
            include: {
              tag: true
            }
          },
          attachments: true,
        }
      });

      // Process tags if provided
      if (tags && tags.length > 0) {
        for (const tagName of tags) {
          // Find or create tag
          let tag = await tx.tag.findFirst({
            where: {
              name: tagName.toLowerCase(),
              userId: req.user!.id
            }
          });

          if (!tag) {
            tag = await tx.tag.create({
              data: {
                name: tagName.toLowerCase(),
                userId: req.user!.id,
                category: 'KEYWORD'
              }
            });
          }

          // Link tag to entry
          await tx.entryTag.create({
            data: {
              entryId: newEntry.id,
              tagId: tag.id,
              confidence: 1.0,
              autoGenerated: false
            }
          });
        }

        // Fetch the entry with tags
        return await tx.entry.findUnique({
          where: { id: newEntry.id },
          include: {
            tags: {
              include: {
                tag: true
              }
            },
            attachments: true,
          }
        });
      }

      return newEntry;
    });

    res.status(201).json({
      message: 'Entry created successfully',
      entry
    });
  } catch (error) {
    console.error('Create entry error:', error);
    res.status(500).json({ error: 'Failed to create entry' });
  }
};

// Get all entries for the authenticated user
export const getEntries = async (req: Request<{}, {}, {}, QueryParams>, res: Response) => {
  try {
    if (!req.user) {
      return res.status(401).json({ error: 'Not authenticated' });
    }

    const {
      page = '1',
      limit = '10',
      search,
      mood,
      startDate,
      endDate,
      tags,
      isFavorite,
      sortBy = 'createdAt',
      sortOrder = 'desc'
    } = req.query;

    const pageNum = parseInt(page);
    const limitNum = parseInt(limit);
    const skip = (pageNum - 1) * limitNum;

    // Build where clause
    const where: any = {
      userId: req.user.id
    };

    // Add search filter
    if (search) {
      where.OR = [
        { title: { contains: search, mode: 'insensitive' } },
        { content: { contains: search, mode: 'insensitive' } }
      ];
    }

    // Add mood filter
    if (mood) {
      where.mood = mood;
    }

    // Add date range filter
    if (startDate || endDate) {
      where.createdAt = {};
      if (startDate) {
        where.createdAt.gte = new Date(startDate);
      }
      if (endDate) {
        where.createdAt.lte = new Date(endDate);
      }
    }

    // Add favorite filter
    if (isFavorite === 'true') {
      where.isFavorite = true;
    }

    // Add tags filter
    if (tags) {
      const tagArray = tags.split(',').map(t => t.trim().toLowerCase());
      where.tags = {
        some: {
          tag: {
            name: {
              in: tagArray
            }
          }
        }
      };
    }

    // Get total count
    const totalCount = await prisma.entry.count({ where });

    // Get entries
    const entries = await prisma.entry.findMany({
      where,
      skip,
      take: limitNum,
      orderBy: {
        [sortBy]: sortOrder
      },
      include: {
        tags: {
          include: {
            tag: true
          }
        },
        attachments: true,
        _count: {
          select: {
            attachments: true,
            tags: true
          }
        }
      }
    });

    res.json({
      entries,
      pagination: {
        page: pageNum,
        limit: limitNum,
        total: totalCount,
        totalPages: Math.ceil(totalCount / limitNum)
      }
    });
  } catch (error) {
    console.error('Get entries error:', error);
    res.status(500).json({ error: 'Failed to fetch entries' });
  }
};

