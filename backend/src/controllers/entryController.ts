// Search entries with full-text search
export const searchEntries = async (req: Request, res: Response) => {
  try {
    if (!req.user) {
      return res.status(401).json({ error: 'Not authenticated' });
    }

    const { q, page = '1', limit = '10' } = req.query;

    if (!q || typeof q !== 'string') {
      return res.status(400).json({ error: 'Search query is required' });
    }

    const pageNum = parseInt(page as string);
    const limitNum = parseInt(limit as string);
    const skip = (pageNum - 1) * limitNum;

    // Use PostgreSQL full-text search if available
    // For now, using simple contains search
    const where = {
      userId: req.user.id,
      OR: [
        { title: { contains: q, mode: 'insensitive' as const } },
        { content: { contains: q, mode: 'insensitive' as const } },
        {
          tags: {
            some: {
              tag: {
                name: { contains: q.toLowerCase(), mode: 'insensitive' as const }
              }
            }
          }
        }
      ]
    };

    const totalCount = await prisma.entry.count({ where });

    const entries = await prisma.entry.findMany({
      where,
      skip,
      take: limitNum,
      orderBy: {
        createdAt: 'desc'
      },
      include: {
        tags: {
          include: {
            tag: true
          }
        },
        attachments: true
      }
    });

    res.json({
      entries,
      query: q,
      pagination: {
        page: pageNum,
        limit: limitNum,
        total: totalCount,
        totalPages: Math.ceil(totalCount / limitNum)
      }
    });
  } catch (error) {
    console.error('Search entries error:', error);
    res.status(500).json({ error: 'Failed to search entries' });
  }
};

// Get entry statistics
export const getEntryStats = async (req: Request, res: Response) => {
  try {
    if (!req.user) {
      return res.status(401).json({ error: 'Not authenticated' });
    }

    const { startDate, endDate } = req.query;

    const where: any = {
      userId: req.user.id
    };

    if (startDate || endDate) {
      where.createdAt = {};
      if (startDate) {
        where.createdAt.gte = new Date(startDate as string);
      }
      if (endDate) {
        where.createdAt.lte = new Date(endDate as string);
      }
    }

    // Get various statistics
    const [
      totalEntries,
      totalWords,
      moodCounts,
      favoriteCount,
      entriesByDay,
      topTags
    ] = await Promise.all([
      // Total entries
      prisma.entry.count({ where }),
      
      // Total words
      prisma.entry.aggregate({
        where,
        _sum: { wordCount: true }
      }),
      
      // Mood distribution
      prisma.entry.groupBy({
        by: ['mood'],
        where,
        _count: true
      }),
      
      // Favorite count
      prisma.entry.count({
        where: { ...where, isFavorite: true }
      }),
      
      // Entries by day (last 30 days)
      prisma.$queryRaw`
        SELECT DATE(created_at) as date, COUNT(*) as count
        FROM "Entry"
        WHERE user_id = ${req.user.id}
          AND created_at >= NOW() - INTERVAL '30 days'
        GROUP BY DATE(created_at)
        ORDER BY date DESC
      `,
      
      // Top tags
      prisma.$queryRaw`
        SELECT t.name, t.id, COUNT(et.id) as usage_count
        FROM "Tag" t
        JOIN "EntryTag" et ON t.id = et.tag_id
        WHERE t.user_id = ${req.user.id}
        GROUP BY t.id, t.name
        ORDER BY usage_count DESC
        LIMIT 10
      `
    ]);

    res.json({
      stats: {
        totalEntries,
        totalWords: totalWords._sum.wordCount || 0,
        averageWords: totalEntries > 0 ? Math.round((totalWords._sum.wordCount || 0) / totalEntries) : 0,
        favoriteCount,
        moodDistribution: moodCounts,
        entriesByDay,
        topTags
      }
    });
  } catch (error) {
    console.error('Get entry stats error:', error);
    res.status(500).json({ error: 'Failed to fetch entry statistics' });
  }
};