// Update an entry
export const updateEntry = async (req: Request<{ id: string }, {}, UpdateEntryRequest>, res: Response) => {
  try {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({ errors: errors.array() });
    }

    if (!req.user) {
      return res.status(401).json({ error: 'Not authenticated' });
    }

    const { id } = req.params;
    const { title, content, mood, privacy, location, weather, tags, isFavorite } = req.body;

    // Check if entry exists and belongs to user
    const existingEntry = await prisma.entry.findFirst({
      where: {
        id,
        userId: req.user.id
      }
    });

    if (!existingEntry) {
      return res.status(404).json({ error: 'Entry not found' });
    }

    // Calculate word count if content is being updated
    const wordCount = content ? content.trim().split(/\s+/).length : existingEntry.wordCount;

    // Update entry in a transaction
    const updatedEntry = await prisma.$transaction(async (tx) => {
      // Update the entry
      const entry = await tx.entry.update({
        where: { id },
        data: {
          title,
          content,
          mood,
          privacy,
          location,
          weather,
          wordCount,
          isFavorite
        }
      });

      // Update tags if provided
      if (tags !== undefined) {
        // Remove all existing tags
        await tx.entryTag.deleteMany({
          where: { entryId: id }
        });

        // Add new tags
        if (tags.length > 0) {
          for (const tagName of tags) {
            // Find or create tag
            let tag = await tx.tag.findFirst({
              where: {
                name: tagName.toLowerCase(),
                userId: req.user!.id
              }
            });

            if (!tag) {
              tag = await tx.tag.create({
                data: {
                  name: tagName.toLowerCase(),
                  userId: req.user!.id,
                  category: 'KEYWORD'
                }
              });
            }

            // Link tag to entry
            await tx.entryTag.create({
              data: {
                entryId: entry.id,
                tagId: tag.id,
                confidence: 1.0,
                autoGenerated: false
              }
            });
          }
        }
      }

      // Return updated entry with relations
      return await tx.entry.findUnique({
        where: { id },
        include: {
          tags: {
            include: {
              tag: true
            }
          },
          attachments: true
        }
      });
    });

    res.json({
      message: 'Entry updated successfully',
      entry: updatedEntry
    });
  } catch (error) {
    console.error('Update entry error:', error);
    res.status(500).json({ error: 'Failed to update entry' });
  }
};

